package ArrayList;

public class Nombres {

    private String nombre; 
	
	
    public Nombres(String nombre){
    
    	this.nombre = nombre;
    	
    }
	
	
    public String getNombre() {
    	
    	return this.nombre;
    }



    public String toString() {
		
		return "Nombre: " + nombre;
	}

  
    
    
}

	
	

